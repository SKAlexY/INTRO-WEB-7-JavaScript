function prepararMateriales() {
    console.log("Preparar los materiales de limpieza, como desinfectantes y toallas.");
}

function esterilizarInstrumentos() {
    console.log("Esterilizar todos los instrumentos y herramientas dentales.");
}

function limpiarSuperficies() {
    console.log("Limpiar y desinfectar todas las superficies, incluyendo sillas y mesas.");
}

function revisarEquipos() {
    console.log("Realizar un control de calidad en los equipos y asegurarse de que funcionen correctamente.");
}

function organizarSuministros() {
    console.log("Organizar los suministros y asegurarse de que haya suficientes para el próximo paciente.");
}

function programarCitas() {
    console.log("Programar las citas de los pacientes para el día siguiente.");
}

function realizarLimpiezaConsultorio() {
    console.log("Iniciando la limpieza del consultorio dental:");
    prepararMateriales();
    esterilizarInstrumentos();
    limpiarSuperficies();
    revisarEquipos();
    organizarSuministros();
    programarCitas();
    console.log("¡La limpieza del consultorio dental ha sido completada!");
}

// Llamar a la función para iniciar el proceso de limpieza
realizarLimpiezaConsultorio();